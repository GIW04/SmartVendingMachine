package id.ac.petra.proyekpatglendy;

public class DataModel {
    int id;
    String loc;

    public DataModel() {
        this.id = id;
        this.loc = loc;
    }

    public int getId(){
        return id;
    }

    public String getLoc(){
        return loc;
    }

    public void setId(int id){
        this.id = id;
    }

    public void setLoc(String loc){
        this.loc = loc;
    }
}
